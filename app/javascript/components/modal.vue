<template lang="pug">
transition(name='modal' v-if="showingModal({ modalName: name })")
  div.modal-mask.fixed.flex.flex-col.items-center.justify-center.w-full.top-0.left-0.vh-100.z1(
    ref='mask'
    @click='handleClickMask'
  )
    div.modal-container.p-8.rounded(
      :style='{ width: width, maxWidth: maxWidth }'
      :class='backgroundClass'
    )
      slot
</template>

<script lang="ts">
import { mapState } from 'pinia';
import { useModalStore } from '@/shared/modal/store';

export default {
  computed: {
    ...mapState(useModalStore, ['showingModal']),
  },

  data() {
    return {
      modalStore: useModalStore(),
    };
  },

  methods: {
    handleClickMask(event: MouseEvent) {
      // make sure we don't close the modal when clicks within the modal propagate up
      if (event.target === this.$refs.mask) {
        this.modalStore.hideModal({ modalName: this.name });
      }
    },

    handleKeydown(event: KeyboardEvent) {
      if (event.key === 'Escape') {
        this.modalStore.hideTopModal();
      }
    },
  },

  created() {
    // since there might be multiple modals, ensure we only register the keydown listener once
    if (!window.davidrunger.modalKeydownListenerRegistered) {
      window.addEventListener('keydown', this.handleKeydown);
      window.davidrunger.modalKeydownListenerRegistered = true;
    }
  },

  unmounted() {
    window.removeEventListener('keydown', this.handleKeydown);
  },

  props: {
    backgroundClass: {
      type: String,
      required: false,
      default: 'bg-white',
    },
    name: {
      type: String,
      required: true,
    },
    width: {
      type: String,
      required: true,
    },
    maxWidth: {
      type: String,
      required: true,
    },
  },
};
</script>

